package model;

/**
 *
 * @author MSII
 */
public class Brand {
    private int id;
    private String name, image, logo;

    public Brand() {
    }

    public Brand(int id, String name, String image, String logo) {
        this.id = id;
        this.name = name;
        this.image = image;
        this.logo = logo;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
    
}
